"""
FeatureDataset — labeled feature vectors for training.

Responsibilities
----------------
1. Load FeatureVector objects from parquet files in storage.features_dir.
2. Join with a labels dict (source_id → label) supplied by the caller.
3. Produce LabeledSample objects consumed by the training loop.
4. Support train/val/test splitting.

Two construction paths
----------------------
from_feature_vectors()  In-memory path — no disk I/O.  Pass FeatureVectors
                        directly from FeaturePipeline.run_batch() along with
                        a labels dict.  Use this for the demo and for runs
                        where you don't need to persist features.

from_storage()          Disk path — reads parquet files written by
                        save_feature_vectors() and a labels CSV.
                        Use this for production runs where feature extraction
                        and training are separate jobs.

Parquet schema
--------------
All scalar FeatureVector fields are stored as columns.  The dmdt 2-D
histogram is excluded — it cannot be stored as a plain parquet column and
is not used by scalar-only models (e.g. LogisticExampleClassifier).
Gaia optional floats are stored as nullable float64; None is round-tripped
through NaN.

Label source
------------
Labels are NOT generated by ml4em.  They come from the researcher's
upstream preparation step (e.g. a Gaia WD catalog cross-match for WDB
science, or an SDSS spectroscopic label file for galaxy classification).
The labels CSV must have columns: source_id (str), label (int 0/1).
"""

from __future__ import annotations

import csv
import dataclasses
import random
from dataclasses import dataclass
from pathlib import Path
from typing import Optional

from ml4em.config.schema import StorageConfig
from ml4em.types import FeatureVector, LabeledSample


@dataclass
class FeatureDataset:
    """A collection of labeled FeatureVectors for model training.

    Attributes
    ----------
    samples:
        Ordered list of (FeatureVector, label) pairs.
    """

    samples: list[LabeledSample]

    def __len__(self) -> int:
        return len(self.samples)

    def __iter__(self):
        return iter(self.samples)

    # ------------------------------------------------------------------
    # Factories
    # ------------------------------------------------------------------

    @classmethod
    def from_feature_vectors(
        cls,
        feature_vectors: list[FeatureVector],
        labels: dict[str, int],
    ) -> "FeatureDataset":
        """Build a labeled dataset directly from in-memory FeatureVectors.

        Use this when feature vectors are already in memory from a fresh
        FeaturePipeline.run_batch() call.  No disk I/O — skips parquet
        entirely.  Ideal for the demo and for single-run pipelines.

        Parameters
        ----------
        feature_vectors:
            Output of FeaturePipeline.run_batch().
        labels:
            Mapping of source_id → label (0 or 1).  Sources not present
            in this dict are silently dropped.

        Returns
        -------
        FeatureDataset
        """
        samples: list[LabeledSample] = []
        for fv in feature_vectors:
            label = labels.get(fv.source_id)
            if label is not None:
                samples.append(LabeledSample(feature=fv, label=label))
        return cls(samples=samples)

    @classmethod
    def from_storage(
        cls,
        storage: StorageConfig,
        labels_path: str,
    ) -> "FeatureDataset":
        """Load a labeled dataset from storage.

        Parameters
        ----------
        storage:
            StorageConfig from PipelineConfig.storage.
            feature_vectors are read from storage.features_dir.
        labels_path:
            Path to a CSV file with columns: source_id, label.
            label must be 0 (negative) or 1 (positive).

        Returns
        -------
        FeatureDataset
            All sources present in both the feature store and the labels file.
            Sources in one but not the other are silently skipped.
        """
        label_map = cls._load_labels(labels_path)
        feature_vectors = cls._load_features(storage.features_dir)

        samples: list[LabeledSample] = []
        for fv in feature_vectors:
            label = label_map.get(fv.source_id)
            if label is not None:
                samples.append(LabeledSample(feature=fv, label=label))

        return cls(samples=samples)

    # ------------------------------------------------------------------
    # Label loading
    # ------------------------------------------------------------------

    @staticmethod
    def _load_labels(labels_path: str) -> dict[str, int]:
        """Read a labels CSV and return {source_id: label} mapping.

        Expected CSV format (with or without header):
            source_id,label
            1234567890,1
            0987654321,0

        Parameters
        ----------
        labels_path:
            Path to the labels CSV file.

        Returns
        -------
        dict[str, int]
            Mapping from source_id string to integer label (0 or 1).
        """
        path = Path(labels_path)
        if not path.exists():
            raise FileNotFoundError(
                f"Labels file not found: {labels_path}\n"
                "Provide a CSV with columns: source_id, label"
            )

        label_map: dict[str, int] = {}
        with open(path, newline="") as f:
            reader = csv.DictReader(f)
            for row in reader:
                source_id = str(row["source_id"]).strip()
                label = int(row["label"])
                if label not in (0, 1):
                    raise ValueError(
                        f"Invalid label {label!r} for source {source_id!r}. "
                        "Labels must be 0 (negative) or 1 (positive)."
                    )
                label_map[source_id] = label

        return label_map

    # ------------------------------------------------------------------
    # Feature persistence
    # ------------------------------------------------------------------

    @staticmethod
    def save_feature_vectors(
        feature_vectors: list[FeatureVector],
        output_path: str,
    ) -> None:
        """Persist a list of FeatureVectors to a parquet file.

        Call this after FeaturePipeline.run_batch() to save features to
        disk so they can be reloaded with from_storage() in a later job —
        avoiding re-running expensive GPU feature extraction.

        The dmdt 2-D histogram is intentionally excluded: it cannot be
        stored as a plain parquet column.  All 42 scalar SCALAR_FIELDS,
        source_id, survey, ra, dec, n_obs, period_algorithm, and the 4
        optional Gaia floats are preserved.  Gaia None values are stored
        as NaN and restored on load.

        Parameters
        ----------
        feature_vectors:
            Output of FeaturePipeline.run_batch().
        output_path:
            Full path to the output parquet file, e.g.
            "features/batch_001.parquet".  Parent directory is created
            if it does not exist.
        """
        import pandas as pd

        rows = []
        for fv in feature_vectors:
            d = dataclasses.asdict(fv)
            d.pop("dmdt", None)   # 2-D array — excluded from parquet schema
            rows.append(d)

        Path(output_path).parent.mkdir(parents=True, exist_ok=True)
        pd.DataFrame(rows).to_parquet(output_path, index=False)

    @staticmethod
    def _load_features(features_dir: str) -> list[FeatureVector]:
        """Load FeatureVector objects from parquet files in features_dir.

        Reads every *.parquet file written by save_feature_vectors() and
        reconstructs FeatureVector objects.  dmdt is not restored (it was
        not saved); scalar-only models (LogisticExampleClassifier, XGBoost)
        are unaffected.

        Parameters
        ----------
        features_dir:
            Directory containing *.parquet files.

        Returns
        -------
        list[FeatureVector]
            All FeatureVectors across all parquet files, in file order.

        Raises
        ------
        FileNotFoundError
            If features_dir does not exist or contains no parquet files.
        """
        import pandas as pd

        path = Path(features_dir)
        if not path.exists():
            raise FileNotFoundError(
                f"Features directory not found: {features_dir}\n"
                "Run FeaturePipeline.run_batch() and save results with\n"
                "FeatureDataset.save_feature_vectors() first."
            )

        parquet_files = sorted(path.glob("*.parquet"))
        if not parquet_files:
            raise FileNotFoundError(
                f"No parquet files found in {features_dir}.\n"
                "Save feature vectors with FeatureDataset.save_feature_vectors()."
            )

        df = pd.concat(
            [pd.read_parquet(f) for f in parquet_files],
            ignore_index=True,
        )

        feature_vectors: list[FeatureVector] = []
        gaia_fields = ("gaia_parallax", "gaia_parallax_error", "gaia_bp_rp", "gaia_ruwe")
        for _, row in df.iterrows():
            d = row.to_dict()
            # Restore NaN → None for optional Gaia floats
            for field in gaia_fields:
                if field in d and pd.isna(d[field]):
                    d[field] = None
            feature_vectors.append(FeatureVector(**d))

        return feature_vectors

    # ------------------------------------------------------------------
    # Splitting
    # ------------------------------------------------------------------

    def split(
        self,
        val_fraction: float = 0.1,
        test_fraction: float = 0.1,
        seed: int = 42,
    ) -> tuple["FeatureDataset", "FeatureDataset", "FeatureDataset"]:
        """Randomly split into (train, val, test) datasets.

        Splitting is done on the full sample list; class balance is not
        preserved across splits (use stratified split if needed).

        Parameters
        ----------
        val_fraction:
            Fraction of samples held out for validation.
        test_fraction:
            Fraction of samples held out for final evaluation.
        seed:
            Random seed for reproducibility.

        Returns
        -------
        (train, val, test) : tuple[FeatureDataset, FeatureDataset, FeatureDataset]
        """
        if not 0.0 < val_fraction < 1.0:
            raise ValueError(f"val_fraction must be in (0, 1), got {val_fraction}")
        if not 0.0 < test_fraction < 1.0:
            raise ValueError(f"test_fraction must be in (0, 1), got {test_fraction}")
        if val_fraction + test_fraction >= 1.0:
            raise ValueError(
                f"val_fraction + test_fraction must be < 1.0, "
                f"got {val_fraction + test_fraction}"
            )

        rng = random.Random(seed)
        shuffled = list(self.samples)
        rng.shuffle(shuffled)

        n = len(shuffled)
        n_test = max(1, int(n * test_fraction))
        n_val  = max(1, int(n * val_fraction))

        test_samples  = shuffled[:n_test]
        val_samples   = shuffled[n_test : n_test + n_val]
        train_samples = shuffled[n_test + n_val :]

        return (
            FeatureDataset(samples=train_samples),
            FeatureDataset(samples=val_samples),
            FeatureDataset(samples=test_samples),
        )

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def class_counts(self) -> dict[int, int]:
        """Return {label: count} for each class in the dataset."""
        counts: dict[int, int] = {}
        for s in self.samples:
            counts[s.label] = counts.get(s.label, 0) + 1
        return counts

    def positive_fraction(self) -> float:
        """Fraction of samples with label == 1."""
        if not self.samples:
            return 0.0
        n_pos = sum(1 for s in self.samples if s.label == 1)
        return n_pos / len(self.samples)
