"""
FeatureDataset — labeled feature vectors for training.

Responsibilities
----------------
1. Load FeatureVector objects from parquet files in storage.features_dir.
2. Join with a labels CSV (source_id → label) supplied by the caller.
3. Produce LabeledSample objects consumed by the training loop.
4. Support train/val/test splitting.

Label source
------------
Labels are NOT generated by ml4em.  They come from the researcher's
upstream preparation step (e.g. a Gaia WD catalog cross-match for WDB
science, or an SDSS spectroscopic label file for galaxy classification).
The labels CSV must have columns: source_id (str), label (int 0/1).

Usage
-----
    from ml4em.training import FeatureDataset
    from ml4em.config import load_config

    cfg = load_config("config.yaml")
    dataset = FeatureDataset.from_storage(cfg.storage, labels_path="labels.csv")
    train, val, test = dataset.split(
        val_fraction=0.1, test_fraction=0.1, seed=42
    )

Status
------
Parquet loading is a stub — the feature layer's output format (parquet
schema) must be finalized before this can be implemented.
Label joining and splitting are implementable now.
"""

from __future__ import annotations

import csv
import random
from dataclasses import dataclass
from pathlib import Path
from typing import Optional

from ml4em.config.schema import StorageConfig
from ml4em.types import FeatureVector, LabeledSample


@dataclass
class FeatureDataset:
    """A collection of labeled FeatureVectors for model training.

    Attributes
    ----------
    samples:
        Ordered list of (FeatureVector, label) pairs.
    """

    samples: list[LabeledSample]

    def __len__(self) -> int:
        return len(self.samples)

    def __iter__(self):
        return iter(self.samples)

    # ------------------------------------------------------------------
    # Factory
    # ------------------------------------------------------------------

    @classmethod
    def from_storage(
        cls,
        storage: StorageConfig,
        labels_path: str,
    ) -> "FeatureDataset":
        """Load a labeled dataset from storage.

        Parameters
        ----------
        storage:
            StorageConfig from PipelineConfig.storage.
            feature_vectors are read from storage.features_dir.
        labels_path:
            Path to a CSV file with columns: source_id, label.
            label must be 0 (negative) or 1 (positive).

        Returns
        -------
        FeatureDataset
            All sources present in both the feature store and the labels file.
            Sources in one but not the other are silently skipped.
        """
        label_map = cls._load_labels(labels_path)
        feature_vectors = cls._load_features(storage.features_dir)

        samples: list[LabeledSample] = []
        for fv in feature_vectors:
            label = label_map.get(fv.source_id)
            if label is not None:
                samples.append(LabeledSample(feature=fv, label=label))

        return cls(samples=samples)

    # ------------------------------------------------------------------
    # Label loading
    # ------------------------------------------------------------------

    @staticmethod
    def _load_labels(labels_path: str) -> dict[str, int]:
        """Read a labels CSV and return {source_id: label} mapping.

        Expected CSV format (with or without header):
            source_id,label
            1234567890,1
            0987654321,0

        Parameters
        ----------
        labels_path:
            Path to the labels CSV file.

        Returns
        -------
        dict[str, int]
            Mapping from source_id string to integer label (0 or 1).
        """
        path = Path(labels_path)
        if not path.exists():
            raise FileNotFoundError(
                f"Labels file not found: {labels_path}\n"
                "Provide a CSV with columns: source_id, label"
            )

        label_map: dict[str, int] = {}
        with open(path, newline="") as f:
            reader = csv.DictReader(f)
            for row in reader:
                source_id = str(row["source_id"]).strip()
                label = int(row["label"])
                if label not in (0, 1):
                    raise ValueError(
                        f"Invalid label {label!r} for source {source_id!r}. "
                        "Labels must be 0 (negative) or 1 (positive)."
                    )
                label_map[source_id] = label

        return label_map

    # ------------------------------------------------------------------
    # Feature loading (stub)
    # ------------------------------------------------------------------

    @staticmethod
    def _load_features(features_dir: str) -> list[FeatureVector]:
        """Load FeatureVector objects from parquet files in features_dir.

        Parameters
        ----------
        features_dir:
            Directory containing parquet files written by FeaturePipeline.

        Returns
        -------
        list[FeatureVector]
            All FeatureVectors found in the directory.
        """
        raise NotImplementedError(
            "FeatureDataset._load_features is not yet implemented.\n"
            "The feature layer must finalize its parquet output schema before\n"
            "this can be implemented.  Once FeaturePipeline.run_batch() writes\n"
            "parquet files to storage.features_dir, implement this to:\n"
            "  1. Glob for '*.parquet' in features_dir.\n"
            "  2. Read each file with pandas or pyarrow.\n"
            "  3. Reconstruct FeatureVector objects from rows.\n"
            "  4. Return the combined list."
        )

    # ------------------------------------------------------------------
    # Splitting
    # ------------------------------------------------------------------

    def split(
        self,
        val_fraction: float = 0.1,
        test_fraction: float = 0.1,
        seed: int = 42,
    ) -> tuple["FeatureDataset", "FeatureDataset", "FeatureDataset"]:
        """Randomly split into (train, val, test) datasets.

        Splitting is done on the full sample list; class balance is not
        preserved across splits (use stratified split if needed).

        Parameters
        ----------
        val_fraction:
            Fraction of samples held out for validation.
        test_fraction:
            Fraction of samples held out for final evaluation.
        seed:
            Random seed for reproducibility.

        Returns
        -------
        (train, val, test) : tuple[FeatureDataset, FeatureDataset, FeatureDataset]
        """
        if not 0.0 < val_fraction < 1.0:
            raise ValueError(f"val_fraction must be in (0, 1), got {val_fraction}")
        if not 0.0 < test_fraction < 1.0:
            raise ValueError(f"test_fraction must be in (0, 1), got {test_fraction}")
        if val_fraction + test_fraction >= 1.0:
            raise ValueError(
                f"val_fraction + test_fraction must be < 1.0, "
                f"got {val_fraction + test_fraction}"
            )

        rng = random.Random(seed)
        shuffled = list(self.samples)
        rng.shuffle(shuffled)

        n = len(shuffled)
        n_test = max(1, int(n * test_fraction))
        n_val  = max(1, int(n * val_fraction))

        test_samples  = shuffled[:n_test]
        val_samples   = shuffled[n_test : n_test + n_val]
        train_samples = shuffled[n_test + n_val :]

        return (
            FeatureDataset(samples=train_samples),
            FeatureDataset(samples=val_samples),
            FeatureDataset(samples=test_samples),
        )

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def class_counts(self) -> dict[int, int]:
        """Return {label: count} for each class in the dataset."""
        counts: dict[int, int] = {}
        for s in self.samples:
            counts[s.label] = counts.get(s.label, 0) + 1
        return counts

    def positive_fraction(self) -> float:
        """Fraction of samples with label == 1."""
        if not self.samples:
            return 0.0
        n_pos = sum(1 for s in self.samples if s.label == 1)
        return n_pos / len(self.samples)
